
public class TypeCasting1 {
    public static void main(String[] args) {
        int a=12;
        double b=a; 
        System.out.println(a);
        System.out.println(b);

        double x=12.6;
        int y=(int)x; 
        System.out.println(x);
        System.out.println(y);
    }
}
